 #3. Python program to convert the temperature in degree centigrade to Fahrenheit

# Input temerature in Celsius
celsius = float(input("Enter temperature in celsius: "))

# Convert to Fahrenheit
fahrenheit = (celsius * 1.8) + 32

# Print the result
print(str(celsius )+ " degree Celsius is equal to " + str(fahrenheit )+ " degree Fahrenheit.")

# OUTPUT:
# Enter temperature in Celsius: 30
# 30.0 degrees Celsius is equal to 86.0 degrees Fahrenheit


