# Q.2) Write a Python program to convert a list to a tuple.

# Input: listx = [5, 10, 7, 4, 15, 3]

# CODE:

# Define the list
listx = [5, 10, 7, 4, 15, 3]

# Convert the list to a tuple
tuplex = tuple(listx)

# Print the result
print(f"The list converted to a tuple is: {tuplex}")

# OUTPUT:
# The list converted to a tuple is: (5, 10, 7, 4, 15, 3)
